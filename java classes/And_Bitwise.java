// Java AND Operator Example: Logical && and Bitwise &


public class And_Bitwise 
{  
    public static void main(String args[])
    {  
    int a=10;  
    int b=5;  
    int c=20;  
    System.out.println(a<b&&a<c);//false && true = false  
    System.out.println(a<b&a<c);//false & true = false  
    }} 
